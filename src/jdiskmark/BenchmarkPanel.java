    public void addRun(Benchmark run) {
        
        List<BenchmarkOperation> operations = run.getOperations();
        DefaultTableModel model = (DefaultTableModel) runTable.getModel();
        for (BenchmarkOperation o : operations) {
            model.addRow(
                    new Object[] {
                        run.getId(),
                        run.getDriveInfo().getDriveModel(),
                        run.getUsageColumnDisplay(),
                        o.getModeDisplay(),
                        o.blockOrder,
                        o.numSamples,
                        o.getBlocksDisplay(),
                        o.numThreads,
                        o.getStartTimeString(),
                        o.getDuration(),
                        o.getAccTimeDisplay(),
                        o.getBwMinMaxDisplay(),
                        o.getBwAvgDisplay(),
                    });
        }
    }
    
    public void hideFirstColumn() {
        TableColumnModel columnModel = runTable.getColumnModel();
        columnModel.removeColumn(columnModel.getColumn(0));
    }
    
    public void clearTable() {
        DefaultTableModel model = (DefaultTableModel) this.runTable.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
    }
    
    public void deselect() {
        runTable.getSelectionModel().clearSelection();
    }
    
    // Get the selected benchmarks
    public List<UUID> getSelectedIds() {
        LinkedList<UUID> ids = new LinkedList<>();
        int[] selectedRows = runTable.getSelectedRows();
        // for each row get the benchmark id in the hidden column
        for (int r : selectedRows) {
            try {
                UUID benchmarkId = (UUID) runTable.getModel().getValueAt(r, 0);
                ids.add(benchmarkId);
            } catch (RuntimeException re) {
                System.err.println("Error retrieving id at row=" + r);
            }
        }
        return ids;
    }
